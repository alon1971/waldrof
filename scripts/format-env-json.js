#!/usr/bin/env node
'use strict';

/**
 * Convert a multi-line Google Service Account JSON file into a single-line
 * string ready to paste into .env as GOOGLE_DRIVE_SERVICE_ACCOUNT_JSON.
 *
 * Usage:
 *   node scripts/format-env-json.js path/to/service-account.json
 *   node scripts/format-env-json.js path/to/service-account.json --write-env
 *   Get-Content sa.json -Raw | node scripts/format-env-json.js
 *
 * --write-env  Updates GOOGLE_DRIVE_SERVICE_ACCOUNT_JSON in local .env
 *              (creates the key if missing). Does not print the private key.
 */

const fs = require('fs');
const path = require('path');

function readInput(argv) {
  const fileArg = argv.find(function (a) {
    return a && a !== '--write-env' && a !== '--help' && a !== '-h' && !a.startsWith('-');
  });
  if (fileArg) {
    const abs = path.resolve(process.cwd(), fileArg);
    if (!fs.existsSync(abs)) {
      throw new Error('File not found: ' + abs);
    }
    return fs.readFileSync(abs, 'utf8');
  }
  if (!process.stdin.isTTY) {
    return fs.readFileSync(0, 'utf8');
  }
  throw new Error(
    'Provide a JSON file path, or pipe JSON via stdin.\n' +
      'Example: node scripts/format-env-json.js ./my-sa.json'
  );
}

function normalizeServiceAccountJson(raw) {
  let text = String(raw || '').trim();
  if (!text) throw new Error('Empty input');

  // Strip accidental wrapping quotes from a whole pasted blob.
  if (
    (text.charAt(0) === '"' && text.charAt(text.length - 1) === '"') ||
    (text.charAt(0) === "'" && text.charAt(text.length - 1) === "'")
  ) {
    text = text.slice(1, -1).trim();
  }

  let parsed;
  try {
    parsed = JSON.parse(text);
  } catch (e) {
    throw new Error('Invalid JSON: ' + (e.message || e));
  }

  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    throw new Error('Service account JSON must be an object');
  }
  if (!parsed.client_email || !parsed.private_key) {
    throw new Error('JSON must include client_email and private_key');
  }

  // Compact single-line JSON (private_key keeps its \\n escapes inside the string).
  return JSON.stringify(parsed);
}

function upsertEnvKey(envPath, key, oneLineJson) {
  // Store without outer quotes — JSON already uses "…". api/env.js strips only
  // a single pair of wrapping quotes if present; nested \" would break parse.
  const line = key + '=' + oneLineJson;
  let content = '';
  if (fs.existsSync(envPath)) {
    content = fs.readFileSync(envPath, 'utf8');
  }
  const re = new RegExp(
    '^\\s*' + key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '\\s*=.*$',
    'm'
  );
  if (re.test(content)) {
    content = content.replace(re, line);
  } else {
    if (content && !/\n$/.test(content)) content += '\n';
    content +=
      '\n# Google Drive service account (generated by scripts/format-env-json.js)\n' +
      line +
      '\n';
  }
  fs.writeFileSync(envPath, content, 'utf8');
}

function printHelp() {
  console.log(
    [
      'format-env-json — pack a Service Account JSON for .env',
      '',
      'Usage:',
      '  node scripts/format-env-json.js <service-account.json>',
      '  node scripts/format-env-json.js <service-account.json> --write-env',
      '  Get-Content sa.json -Raw | node scripts/format-env-json.js',
      '',
      'Paste into .env (prefer no wrapping quotes around the JSON object):',
      '  GOOGLE_DRIVE_SERVICE_ACCOUNT_JSON={"type":"service_account",...}',
      '  GOOGLE_DRIVE_CATALOG_ROOT_FOLDER_ID=<folder id from Drive URL>',
      '',
      'Share the Drive catalog folder with the service-account client_email (Viewer).',
      'Verify: node scripts/probe-drive-community-search.js',
    ].join('\n')
  );
}

function main() {
  const argv = process.argv.slice(2);
  if (argv.indexOf('--help') >= 0 || argv.indexOf('-h') >= 0) {
    printHelp();
    process.exit(0);
  }

  try {
    const raw = readInput(argv);
    const oneLine = normalizeServiceAccountJson(raw);
    const writeEnv = argv.indexOf('--write-env') >= 0;

    if (writeEnv) {
      const envPath = path.join(__dirname, '..', '.env');
      upsertEnvKey(envPath, 'GOOGLE_DRIVE_SERVICE_ACCOUNT_JSON', oneLine);
      const parsed = JSON.parse(oneLine);
      console.log('[format-env-json] Updated GOOGLE_DRIVE_SERVICE_ACCOUNT_JSON in .env');
      console.log('[format-env-json] client_email =', parsed.client_email);
      console.log('[format-env-json] Share your catalog folder with that email (Viewer).');
      console.log('[format-env-json] Also set GOOGLE_DRIVE_CATALOG_ROOT_FOLDER_ID if still empty.');
      process.exit(0);
    }

    // Print ready-to-paste .env assignment (JSON starts with { — no outer quotes).
    process.stdout.write('GOOGLE_DRIVE_SERVICE_ACCOUNT_JSON=' + oneLine + '\n');
  } catch (err) {
    console.error('[format-env-json] ERROR:', err && err.message ? err.message : err);
    process.exit(1);
  }
}

main();
